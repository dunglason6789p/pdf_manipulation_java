package mainPackage.model;

public class MyTextData {
    public String kanji;
    public String hiragana;
    public String nom;
    public String meaning;
    public MyTextData() {}
    public MyTextData(String kanji, String hiragana, String nom, String meaning) {
        this.kanji = kanji;
        this.hiragana = hiragana;
        this.nom = nom;
        this.meaning = meaning;
    }
}
